package Array;

public class day3 {
    static void printarr(int[] arr){
        for(int i=0;i<arr.length;i++){
            System.out.println(arr[i]+" ");
            //System.out.println();
        }
    }
    public static void main(String[] args) {
        int[] arr={3,6,7,8,9};
        printarr(arr);
    }
}
